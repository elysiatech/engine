/**
 * Mouse Key Code
 * @group Events
 */
export var MouseCode;
(function (MouseCode) {
    /**
     *
     * key code of mouse left
     *
     */
    MouseCode[MouseCode["MOUSE_LEFT"] = 0] = "MOUSE_LEFT";
    /**
     *
     * key code of mouse middle
     *
     */
    MouseCode[MouseCode["MOUSE_MID"] = 1] = "MOUSE_MID";
    /**
     *
     * key code of mouse right
     *
     */
    MouseCode[MouseCode["MOUSE_RIGHT"] = 2] = "MOUSE_RIGHT";
})(MouseCode || (MouseCode = {}));
