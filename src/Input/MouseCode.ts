/**
 * Mouse Key Code
 */
export enum MouseCode
{
	/**
	 * key code of mouse left
	 */
	MOUSE_LEFT = 0,

	/**
	 * key code of mouse middle
	 */
	MOUSE_MID = 1,

	/**
	 * key code of mouse right
	 */
	MOUSE_RIGHT = 2,

	/**
	 * key code of mouse four (back)
	 */
	MOUSE_FOUR = 3,

	/**
	 * key code of mouse five (forward)
	 */
	MOUSE_FIVE = 4,
}